    // MARK: - DB 조회
    static func lookup(text: String, from src: String, to tgt: String) -> String? {
        let trimmed = text.trimmingCharacters(in: .whitespacesAndNewlines)
        guard !trimmed.isEmpty else { return nil }

        // 정규화: 반복 문자 축소 (ㅋㅋㅋ/ㅎㅎㅎ/ㅠㅠㅠ → ㅋㅋ/ㅎㅎ/ㅠㅠ).
        // 한국어 source 에서만 의미가 있지만, 다른 source 에 적용해도
        // 표적 문자가 없으면 no-op 이므로 분기 위로 끌어올렸다.
        var normalized = trimmed
        normalized = normalized.replacingOccurrences(of: "ㅋㅋㅋㅋ", with: "ㅋㅋ")
        normalized = normalized.replacingOccurrences(of: "ㅋㅋㅋ", with: "ㅋㅋ")
        normalized = normalized.replacingOccurrences(of: "ㅎㅎㅎㅎ", with: "ㅎㅎ")
        normalized = normalized.replacingOccurrences(of: "ㅎㅎㅎ", with: "ㅎㅎ")
        normalized = normalized.replacingOccurrences(of: "ㅠㅠㅠ", with: "ㅠㅠ")
        normalized = normalized.replacingOccurrences(of: "ㅜㅜㅜ", with: "ㅜㅜ")

        // 이모지 제거. `> 0x238C` 가드로 ?/!/~/한글 자모 같은 저영역
        // 스칼라(일부가 isEmoji=true 로 보고됨)는 보존하고, 실제 그림
        // 이모지(😂🌈❤️ 등 고영역)만 걸러낸다.
        normalized = normalized.unicodeScalars
            .filter { !($0.properties.isEmoji && $0.value > 0x238C) }
            .map { String($0) }
            .joined()

        // 반복 구두점 축소.
        normalized = normalized.replacingOccurrences(of: "...", with: ".")
        normalized = normalized.replacingOccurrences(of: "???", with: "?")
        normalized = normalized.replacingOccurrences(of: "!!!", with: "!")
        normalized = normalized.replacingOccurrences(of: "~~", with: "~")

        // 공백 정규화: 여러 칸 → 단일 칸 + 앞뒤 trim. 이모지 제거 과정에서
        // 새로 생긴 이중 공백도 여기서 함께 정리된다.
        normalized = normalized
            .split(separator: " ", omittingEmptySubsequences: true)
            .joined(separator: " ")

        // 정방향: 한국어 → 타언어.
        if src.contains("한국어") || src.contains("Korean") {
            switch true {
            case tgt.contains("English"):    return stepLookup(normalized, in: koEn)
            case tgt.contains("Japanese"):   return stepLookup(normalized, in: koJa)
            case tgt.contains("Chinese"):    return stepLookup(normalized, in: koZh)
            case tgt.contains("Spanish"):    return stepLookup(normalized, in: koEs)
            case tgt.contains("French"):     return stepLookup(normalized, in: koFr)
            case tgt.contains("German"):     return stepLookup(normalized, in: koDe)
            case tgt.contains("Vietnamese"): return stepLookup(normalized, in: koVi)
            case tgt.contains("Thai"):       return stepLookup(normalized, in: koTh)
            case tgt.contains("Indonesian"): return stepLookup(normalized, in: koId)
            default: return nil
            }
        }

        // 역방향: 타언어 → 한국어.
        if tgt.contains("Korean") || tgt.contains("한국어") {
            switch true {
            case src.contains("English"):
                // English source: enKo 키는 모두 소문자로 저장되어 있다.
                // 1차로 원본 케이스 그대로 시도(이미 모두 소문자로 친 경우
                // 추가 lowercased 비용을 피함), 미스면 소문자로 fallback.
                if let result = stepLookup(normalized, in: enKo) { return result }
                let lower = normalized.lowercased()
                if lower != normalized,
                   let result = stepLookup(lower, in: enKo) { return result }
                return nil
            case src.contains("Japanese"):   return stepLookup(normalized, in: jaKo)
            case src.contains("Chinese"):    return stepLookup(normalized, in: zhKo)
            case src.contains("Spanish"):    return stepLookup(normalized, in: esKo)
            case src.contains("French"):     return stepLookup(normalized, in: frKo)
            case src.contains("German"):     return stepLookup(normalized, in: deKo)
            case src.contains("Vietnamese"): return stepLookup(normalized, in: viKo)
            case src.contains("Thai"):       return stepLookup(normalized, in: thKo)
            case src.contains("Indonesian"): return stepLookup(normalized, in: idKo)
            default: return nil
            }
        }

        return nil
    }

    /// 1차 정확 매칭 → 끝의 ?/!/~/。/？/！ 를 한 글자씩 벗기며 매 단계
    /// 재조회. 정/역방향 양쪽에서 공통으로 사용. 키 자체에 마침표가
    /// 포함된 일/중 표현("信じられない。", "你好吗？" 등)은 1차에서
    /// hit 되므로 잘못 깎아내는 일은 없다.
    ///
    /// 마지막으로, 끝에 붙은 초성 줄임말 run (ㅋㅋ/ㅎㅎ/ㅠㅠ/ㅜㅜ)을
    /// 통째로 떼어내고 한 번 더 조회한다 — "오늘 뭐해ㅋㅋ" → "오늘 뭐해".
    private static func stepLookup(_ key: String, in map: [String: String]) -> String? {
        if let result = map[key] { return result }
        var stripped = key
        while let last = stripped.last, "?!~。？！".contains(last) {
            stripped.removeLast()
            if let result = map[stripped] { return result }
        }
        // 끝에 붙은 ㅋ/ㅎ/ㅠ/ㅜ run 제거 후 재조회. run 을 떼면 그 앞에
        // 남아있던 공백/구두점도 다시 정리한 뒤 마지막으로 한 번 조회한다.
        let fillerChars: Set<Character> = ["ㅋ", "ㅎ", "ㅠ", "ㅜ"]
        if let last = stripped.last, fillerChars.contains(last) {
            while let last = stripped.last, fillerChars.contains(last) {
                stripped.removeLast()
            }
            while let last = stripped.last,
                  last == " " || "?!~。？！".contains(last) {
                stripped.removeLast()
            }
            if let result = map[stripped] { return result }
        }
        return nil
    }

