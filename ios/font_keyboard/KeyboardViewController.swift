    // MARK: - Hangul Composition Engine

    private func hgCompose() -> String {
        if hgCho >= 0 && hgJung >= 0 {
            return String(UnicodeScalar(0xAC00 + hgCho * 21 * 28 + hgJung * 28 + hgJong)!)
        } else if hgCho >= 0 {
            return CHO[hgCho]
        }
        return ""
    }

    private func hgFlush() { hgCho = -1; hgJung = -1; hgJong = 0 }

    private func hgReplaceLast(_ s: String) {
        if !translationInput.isEmpty { translationInput.removeLast() }
        translationInput += s
    }

    private func handleHangulInput(_ key: String) {
        let ci = CHO.firstIndex(of: key)   // chosung index or nil
        let ji = JUNG.firstIndex(of: key)  // jungsung index or nil
        let isCon = ci != nil
        let isVow = ji != nil

        // STATE 0: Empty buffer
        if hgCho < 0 {
            if isCon {
                hgCho = ci!
                translationInput += hgCompose()
            } else if isVow {
                hgFlush()
                translationInput += key
            }
            return
        }

        // STATE 1: Chosung only (no jungsung yet)
        if hgJung < 0 {
            if isVow {
                hgJung = ji!
                hgReplaceLast(hgCompose())
            } else if isCon {
                hgFlush()
                hgCho = ci!
                translationInput += hgCompose()
            }
            return
        }

        // STATE 2: Cho + Jung (no jongsung)
        if hgJong == 0 {
            if isVow {
                // Try compound vowel
                if let cj = CJ["\(hgJung),\(ji!)"] {
                    hgJung = cj
                    hgReplaceLast(hgCompose())
                    return
                }
                // Can't compound вҶ’ flush, output standalone vowel
                hgFlush()
                translationInput += key
            } else if isCon {
                // Try as jongsung
                let jIdx = JONG.firstIndex(of: key)
                if let jIdx = jIdx, jIdx > 0 {
                    hgJong = jIdx
                    hgReplaceLast(hgCompose())
                } else {
                    // Not valid jong вҶ’ flush, new cho
                    hgFlush()
                    hgCho = ci!
                    translationInput += hgCompose()
                }
            }
            return
        }

        // STATE 3: Cho + Jung + Jong
        if isVow {
            // Vowel after jong вҶ’ split jong off as new cho
            if let split = JSP[hgJong] {
                // Compound jong: split into (remaining jong, new cho)
                hgJong = split.0
                hgReplaceLast(hgCompose())
                hgFlush()
                hgCho = split.1
                hgJung = ji!
                translationInput += hgCompose()
            } else if let newCho = J2C[hgJong] {
                // Simple jong вҶ’ becomes new cho
                hgJong = 0
                hgReplaceLast(hgCompose())
                hgFlush()
                hgCho = newCho
                hgJung = ji!
                translationInput += hgCompose()
            } else {
                hgFlush()
                translationInput += key
            }
        } else if isCon {
            // Try compound jongsung
            if let ck = CK["\(hgJong),\(key)"] {
                hgJong = ck
                hgReplaceLast(hgCompose())
                return
            }
            // Can't compound вҶ’ flush, new cho
            hgFlush()
            hgCho = ci!
            translationInput += hgCompose()
        }
    }

    private func handleHangulDelete() {
        if hgCho < 0 {
            // No composition вҖ” just remove last char
            if !translationInput.isEmpty { translationInput.removeLast() }
            return
        }
        if hgJong > 0 {
            // Remove jongsung (check compound first)
            if let split = JSP[hgJong] {
                hgJong = split.0
            } else {
                hgJong = 0
            }
            hgReplaceLast(hgCompose())
        } else if hgJung >= 0 {
            // Remove jungsung (check compound first)
            var found = false
            for (k, v) in CJ where v == hgJung {
                let parts = k.split(separator: ",")
                hgJung = Int(parts[0])!
                found = true
                break
            }
            if !found { hgJung = -1 }
            hgReplaceLast(hgCompose())
            if hgJung < 0 && hgCho >= 0 {
                // Only cho left вҖ” show as jamo
                hgReplaceLast(hgCompose())
            }
        } else {
            // Remove chosung
            if !translationInput.isEmpty { translationInput.removeLast() }
            hgFlush()
        }
    }

    @objc private func translateTriggered() {
        guard !translationInput.isEmpty else { return }

        // Daily limit check with proper date reset
        let defaults = UserDefaults.standard
        let today = todayString()
        let savedDate = defaults.string(forKey: "translation_date") ?? ""
        if today != savedDate {
            defaults.set(0, forKey: "translation_count")
            defaults.set(today, forKey: "translation_date")
        }
        let count = defaults.integer(forKey: "translation_count")
        if count >= dailyFreeTranslations {
            showToast("кө¬лҸ…н•ҳл©ҙ л¬ҙм ңн•ң лІҲм—ӯмқҙ к°ҖлҠҘн•ҙмҡ” вңЁ")
            return
        }

        translateResultLabel?.text = "лІҲм—ӯ мӨ‘..."
        translateResultLabel?.textColor = .darkGray

        let targetLang = translateLangs[translateLangIndex].1
        let body: [String: Any] = [
            "model": "gpt-4o-mini",
            "messages": [
                ["role": "system", "content": "You are a translator. Translate the given text to \(targetLang). Return only the translated text without any explanation."],
                ["role": "user", "content": translationInput],
            ],
            "max_tokens": 500,
        ]
        guard let jsonData = try? JSONSerialization.data(withJSONObject: body),
              let url = URL(string: "https://api.openai.com/v1/chat/completions")
        else { return }

        var request = URLRequest(url: url)
        request.httpMethod = "POST"
        request.setValue("Bearer \(openAIKey)", forHTTPHeaderField: "Authorization")
        request.setValue("application/json", forHTTPHeaderField: "Content-Type")
        request.httpBody = jsonData

        URLSession.shared.dataTask(with: request) { [weak self] data, _, _ in
            DispatchQueue.main.async {
                guard let self = self else { return }
                guard let data = data,
                      let json = try? JSONSerialization.jsonObject(with: data) as? [String: Any],
                      let choices = json["choices"] as? [[String: Any]],
                      let message = choices.first?["message"] as? [String: Any],
                      let translated = message["content"] as? String
                else {
                    self.translateResultLabel?.text = "лІҲм—ӯ мӢӨнҢЁ. лӢӨмӢң мӢңлҸ„н•ҙмЈјм„ёмҡ”."
                    self.translateResultLabel?.textColor = .systemRed
                    return
                }
                self.lastTranslation = translated.trimmingCharacters(in: .whitespacesAndNewlines)
                self.translateResultLabel?.text = self.lastTranslation
                self.translateResultLabel?.textColor = .darkText
                defaults.set(count + 1, forKey: "translation_count")
            }
        }.resume()
    }

