    override func viewDidLoad() {
        super.viewDidLoad()
        view.backgroundColor = .white

        // Clean up old per-date translation keys and reset
        let defaults = UserDefaults.standard
        let today = todayString()
        if defaults.string(forKey: "translation_date") != today {
            defaults.set(0, forKey: "translation_count")
            defaults.set(today, forKey: "translation_date")
        }
        // Remove any stale per-date keys from old implementation
        for key in defaults.dictionaryRepresentation().keys where key.hasPrefix("translation_count_") {
            defaults.removeObject(forKey: key)
        }

        setupLayout()
        showMode(.fonts)
    }

